		<h1 class="flex-auto">Stable Diffusion</h1>
		<p>
			Stable Diffusion is a deep learning, text-to-image model released in 2022. It is primarily
			used to generate detailed images conditioned on text descriptions, though it can also be
			applied to other tasks such as inpainting, outpainting, and generating image-to-image
			translations guided by a text prompt.
		</p>
